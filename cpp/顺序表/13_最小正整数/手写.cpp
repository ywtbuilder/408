/*
题目：
给定一个含 n（n >= 1）个整数的数组，
请设计一个在时间上尽可能高效的算法，找出数组中未出现的最小正整数。

例如：
数组 {-5, 3, 2, 3} 中未出现的最小正整数是 1。
数组 {1, 2, 3} 中未出现的最小正整数是 4。

要求：
1. 给出算法的基本设计思想。
2. 根据设计思想，采用 C 或 C++ 语言描述算法，关键之处给出注释。
3. 说明所设计算法的时间复杂度和空间复杂度。
*/

/*
（1）算法基本思想：
长度为 n 的数组中，未出现的最小正整数只可能在 1 到 n + 1 之间。
因为若 1 到 n 都出现了，则答案就是 n + 1；
否则，1 到 n 中第一个没有出现的数就是答案。

因此开辟一个长度为 n 的标记数组 mark，
mark[i] 表示正整数 i + 1 是否在原数组中出现过。
扫描原数组 A，若 A[i] 在 1 到 n 之间，则将 mark[A[i] - 1] 置为 true。
最后从前向后扫描 mark，第一个为 false 的位置 i，答案就是 i + 1；
若 mark 全部为 true，则答案为 n + 1。
*/

int FindMinMissingPositive(int A[], int n) {
    bool mark[n + 1] = {false};

    for (int i = 0; i < n; i++) {
        // 只标记 1 到 n 之间的正整数，避免 mark 数组越界
        if (A[i] > 0 && A[i] <= n) {
            mark[A[i]] = true;
        }
    }

    for (int i = 1; i <= n; i++) {
        if (mark[i] == false) {
            return i;
        }
    }

    // return i+1;这里 i 已经离开 for 循环作用域了，不能用。
    return n + 1;
}

