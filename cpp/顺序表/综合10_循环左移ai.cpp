// （1）算法基本思想：
// 将一维数组 R 中的序列循环左移 p 个位置，可以看作是把数组 R 划分为两部分：
// 长度为 p 的子序列 A (X0, X1, ..., Xp-1) 和长度为 n-p 的子序列 B (Xp, Xp+1, ..., Xn-1)，即 R = AB。
// 循环左移 p 位后的目标序列为 BA。
// 可以利用数组逆置（反转）的思想，通过以下三步实现：
// 1. 将前 p 个元素逆置，即逆置 A 得到 A^(-1)，此时序列为 A^(-1)B。
// 2. 将后 n-p 个元素逆置，即逆置 B 得到 B^(-1)，此时序列为 A^(-1)B^(-1)。
// 3. 将整个数组逆置，即 (A^(-1)B^(-1))^(-1) = BA，从而得到目标序列。
// 这种三步逆置法只需常数个辅助变量，且每个元素最多被移动两次，效率极高。

// （2）数据结构定义：
// 本题使用 C++ 原生一维数组，可以直接通过指针或引用进行操作，无需额外定义复杂的结构体。
// 数据元素类型为 int。

// （3）算法实现：
#include <iostream>

// 辅助函数：将数组 R 中从 from 到 to 范围内的元素进行逆置
void Reverse(int R[], int from, int to) {
    int temp;
    // 双指针法向中间收缩，交换两端元素
    for (int i = 0; i < (to - from + 1) / 2; i++) {
        temp = R[from + i];
        R[from + i] = R[to - i];
        R[to - i] = temp;
    }
}

// 主算法：将长度为 n 的数组 R 循环左移 p 个位置
void Converse(int R[], int n, int p) {
    // 边界条件处理，防止越界或无效移动
    if (p <= 0 || p >= n || n <= 1) {
        return;
    }
    Reverse(R, 0, p - 1);       // 1. 逆置前 p 个元素 (X0, ..., Xp-1)
    Reverse(R, p, n - 1);       // 2. 逆置后 n-p 个元素 (Xp, ..., Xn-1)
    Reverse(R, 0, n - 1);       // 3. 逆置整个序列 (0, ..., n-1)
}

// （4）复杂度分析：
// 时间复杂度：O(n)。
// 三次逆置操作中，前两个子数组逆置总共进行了 n/2 次交换，最后的整体逆置进行了 n/2 次交换。
// 故总的时间复杂度为 O(n)。
// 空间复杂度：O(1)。
// 算法仅使用了 temp 等常数个辅助变量，没有申请额外的数组空间。


