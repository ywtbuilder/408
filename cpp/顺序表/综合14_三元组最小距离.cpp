/*
题目：
定义三元组(a, b, c)的距离 D = |a - b| + |b - c| + |c - a|。
给定 3 个非空整数集合 S1、S2 和 S3，按升序分别存储在 3 个数组中。
请设计一个尽可能高效的算法，计算并输出所有可能的三元组
(a, b, c)（a ∈ S1, b ∈ S2, c ∈ S3）中的最小距离。

例如：
S1 = {-1, 0, 9}
S2 = {-25, -10, 10, 11}
S3 = {2, 9, 17, 30, 41}
最小距离为 2，相应的三元组为 (9, 10, 9)。

要求：
（1）给出算法的基本设计思想。
（2）根据设计思想，采用 C 或 C++ 语言描述算法，关键之处给出注释。
（3）说明你所设计算法的时间复杂度和空间复杂度。
*/

#include <iostream>
using namespace std;

/*
（1）算法基本思想：
对任意三个数 a、b、c，设其中最大值为 max，最小值为 min。
则三元组距离：
D = |a - b| + |b - c| + |c - a| = 2 * (max - min)。

因此问题转化为：从三个有序数组中各取一个数，使这三个数的最大值和最小值之差最小。

使用三个下标 i、j、k 分别指向 S1、S2、S3 的当前元素。
每次计算当前三元组的最大值、最小值和距离，并更新最小距离。
为了让 max - min 变小，应移动当前最小值所在数组的下标，因为：
若移动最大值或中间值，只会使最大值与最小值的差距不变或变大；
只有让当前最小值变大，才可能缩小差距。
当任意一个数组扫描结束时，算法结束。
*/

/*
（2）数据结构定义：
题目已经说明 3 个集合分别用升序数组存储，因此算法直接使用一维数组及其长度。
*/

/*
（3）算法实现：
*/

int minD = 9999;

int MinDistance(int S1[], int n1, int S2[], int n2, int S3[], int n3) {
    int i = 0, j = 0, k = 0;

    while (i < n1 && j < n2 && k < n3) {
        int min = S1[i];
        int max = S1[i];

        if (S1[i] <= min) {
            min = S1[i];
        }
        if (S2[j] <= min) {
            min = S2[j];
        }
        if (S3[k] <= min) {
            min = S3[k];
        }

        if (S1[i] >= max) {
            max = S1[i];
        }
        if (S2[j] >= max) {
            max = S2[j];
        }
        if (S3[k] >= max) {
            max = S3[k];
        }

        int d = 2 * (max - min);
        if (d < minD) {
            minD = d;
        }
        //不建议用连续的if,如果s1[i]和s2[j]相同，会出问题，不过问题不大
        if (min == S1[i]) {
            i++;
        } else if (min == S2[j]) {
            j++;
        } else {
            k++;
        }
    }

    return minD;
}

/*
（4）复杂度分析：
时间复杂度：O(n1 + n2 + n3)，其中 n1、n2、n3 分别为三个数组的长度。
算法使用三个下标 i、j、k 分别扫描 S1、S2、S3。每次循环至少有一个下标向后移动一位，
且三个下标都只会单调递增，不会回退。因此，三个数组中的元素最多各被扫描一遍，
总的循环次数不超过 n1 + n2 + n3，所以时间复杂度为 O(n1 + n2 + n3)。

空间复杂度：O(1)。
算法只使用 i、j、k、min、max、d 等常数个辅助变量，
没有申请与数组长度 n1、n2、n3 相关的额外存储空间，所以空间复杂度为 O(1)。
*/
