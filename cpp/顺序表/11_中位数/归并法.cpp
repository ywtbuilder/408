int FindMedian(int A[], int B[], int length) {
    int C[2*length];
    int i=0,j=0,k=0;
    //二路合并
    while(i<length&&j<length){
        if(A[i]<B[j]){C[k++]=A[i++];}
        else{C[k++]=B[j++];}
    }
    //一路合并
    while(i<length){
        C[k++]=A[i++];
    }
    
    while(j<length){
        C[k++]=B[j++];
    }
    
    // return C[2*length/2];
    return C[length-1];
}

