/*
题目：
定义三元组(a, b, c)的距离 D = |a - b| + |b - c| + |c - a|。
给定 3 个非空整数集合 S1、S2 和 S3，按升序分别存储在 3 个数组中。
请设计一个尽可能高效的算法，计算并输出所有可能的三元组
(a, b, c)（a ∈ S1, b ∈ S2, c ∈ S3）中的最小距离。

例如：
S1 = {-1, 0, 9}
S2 = {-25, -10, 10, 11}
S3 = {2, 9, 17, 30, 41}
最小距离为 2，相应的三元组为 (9, 10, 9)。

要求：
（1）给出算法的基本设计思想。
（2）根据设计思想，采用 C 或 C++ 语言描述算法，关键之处给出注释。
（3）说明你所设计算法的时间复杂度和空间复杂度。
*/

#include <iostream>
using namespace std;

/*
（1）算法基本思想（暴力法）：
三重循环分别枚举 S1、S2、S3 中的每一个元素，组成所有可能的三元组 (a, b, c)。
对每个三元组，先求出其中的最大值 max 和最小值 min，
根据 D = |a - b| + |b - c| + |c - a| = 2 * (max - min) 计算距离，
并用 minD 记录当前已经找到的最小距离。

暴力法会检查所有可能的三元组，思路直接，但没有利用三个数组已经升序这一条件。
*/

/*
（2）数据结构定义：
题目已经说明 3 个集合分别用升序数组存储，因此算法直接使用一维数组及其长度。
*/

/*
（3）算法实现：
*/
int MinDistance(int A[], int B[], int C[], int n1, int n2, int n3) {
    int minD = 99999;

    for (int i = 0; i < n1; i++) {
        for (int j = 0; j < n2; j++) {
            for (int k = 0; k < n3; k++) {
                int a = A[i], b = B[j], c = C[k];
                int min = a, max = a;

                if (a < min) {
                    min = a;
                }
                if (b < min) {
                    min = b;
                }
                if (c < min) {
                    min = c;
                }

                if (a > max) {
                    max = a;
                }
                if (b > max) {
                    max = b;
                }
                if (c > max) {
                    max = c;
                }

                int d = 2 * (max - min);
                if (d < minD) {
                    minD = d;
                }
            }
        }
    }
    return minD;
}

/*
（4）复杂度分析：
时间复杂度：O(n1 * n2 * n3)，其中 n1、n2、n3 分别为三个数组的长度。
算法使用三重循环枚举所有可能的三元组。外层循环执行 n1 次，
中层循环对每个 i 执行 n2 次，内层循环对每组 i、j 执行 n3 次，
因此总共需要检查 n1 * n2 * n3 个三元组。
每个三元组只进行常数次比较和计算，所以时间复杂度为 O(n1 * n2 * n3)。

空间复杂度：O(1)。
算法只使用 i、j、k、a、b、c、min、max、d、minD 等常数个辅助变量，
没有申请与数组长度 n1、n2、n3 相关的额外存储空间，所以空间复杂度为 O(1)。
*/
